    async function connectWallet() {
        if (window.ethereum) {
            try {
                await window.ethereum.request({ method: "eth_requestAccounts" });
                web3 = new Web3(window.ethereum);
                contract = new web3.eth.Contract(contractABI, contractAddress);
                contractUSDT = new web3.eth.Contract(contractUsdtABI, contractUSDTAddress)
                const accounts = await web3.eth.getAccounts();

                await web3.eth.getBalance(accounts[0]).then(balance => {
                    // console.log(balance);
                    console.log("Balance Of BNB: " + web3.utils.fromWei(balance, 'ether')); // Chuyển đổi số dư từ Wei sang Ether để dễ đọc.
                });

                account = accounts[0];
            } catch (error) {
                console.error("User denied account access", error);
            }
        } else {
            console.error("No Ethereum browser extension detected, install MetaMask on desktop or visit from a dApp browser on mobile.");
        }
    }

    async function getBalanceUSDT() {
        // const account = await connectWallet();
        const balance = await contractUSDT.methods.balanceOf(account).call();
        console.log("Balance Of USDT: " + web3.utils.fromWei(balance, 'ether'));
    }
    
